package com.visualspider.admin;

public class ArticleMappingForm {

    private Long sourceUrlFieldId;
    private Long titleFieldId;
    private Long authorFieldId;
    private Long publishedAtFieldId;
    private Long summaryFieldId;
    private Long contentFieldId;
    private Long coverImageFieldId;

    public Long getSourceUrlFieldId() {
        return sourceUrlFieldId;
    }

    public void setSourceUrlFieldId(Long sourceUrlFieldId) {
        this.sourceUrlFieldId = sourceUrlFieldId;
    }

    public Long getTitleFieldId() {
        return titleFieldId;
    }

    public void setTitleFieldId(Long titleFieldId) {
        this.titleFieldId = titleFieldId;
    }

    public Long getAuthorFieldId() {
        return authorFieldId;
    }

    public void setAuthorFieldId(Long authorFieldId) {
        this.authorFieldId = authorFieldId;
    }

    public Long getPublishedAtFieldId() {
        return publishedAtFieldId;
    }

    public void setPublishedAtFieldId(Long publishedAtFieldId) {
        this.publishedAtFieldId = publishedAtFieldId;
    }

    public Long getSummaryFieldId() {
        return summaryFieldId;
    }

    public void setSummaryFieldId(Long summaryFieldId) {
        this.summaryFieldId = summaryFieldId;
    }

    public Long getContentFieldId() {
        return contentFieldId;
    }

    public void setContentFieldId(Long contentFieldId) {
        this.contentFieldId = contentFieldId;
    }

    public Long getCoverImageFieldId() {
        return coverImageFieldId;
    }

    public void setCoverImageFieldId(Long coverImageFieldId) {
        this.coverImageFieldId = coverImageFieldId;
    }
}

